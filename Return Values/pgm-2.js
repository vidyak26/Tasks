// Function to greet a person by name
var getHelloTo = function (name1, name2) {
    return "Hello to " + name1 + " and " + name2;
};

// Calling getHelloTo with two names and storing the returned message
var fullMessage = getHelloTo("Kandra", "Dax");

// Logging the full message to the console
console.log(fullMessage);

